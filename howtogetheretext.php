<h2>By Luas</h2>
<p>The closest Luas stop to our building is called Museum and it is served by the Red Line.</p>

<p>The video below will show you how to get from the Luas stop to our location. A written description is also available and can be found under the video.</p>

<p>The Luas stop is in front of an entrance to the National Museum of Ireland - Decorative Arts & History. Enter through the stoned arch and take the stairs on your right. At the top of the steps, take a left. After approximately 5 meters (at the end of the metal rail), take a right and continue straight on. Cross the parking lot, walking along the white building. Follow the road as it curbs to the left past a long brown building made of stone. Following the pedestrian path, take a right turn right after the aforementioned building. Go up the stairs with the handrail on your left, continue straight.
Once you have past the metal gate, cross the street and take a left. Continue straight for 20 meters and you will find the Meabhrach Escape Room on your right.</p>

